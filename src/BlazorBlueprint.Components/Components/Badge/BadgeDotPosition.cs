namespace BlazorBlueprint.Components;

/// <summary>
/// Defines the position of the notification dot on a Badge component.
/// </summary>
public enum BadgeDotPosition
{
    /// <summary>
    /// Positions the dot at the top-right corner of the badge.
    /// </summary>
    TopRight,

    /// <summary>
    /// Positions the dot at the top-left corner of the badge.
    /// </summary>
    TopLeft,

    /// <summary>
    /// Positions the dot at the bottom-right corner of the badge.
    /// </summary>
    BottomRight,

    /// <summary>
    /// Positions the dot at the bottom-left corner of the badge.
    /// </summary>
    BottomLeft
}
