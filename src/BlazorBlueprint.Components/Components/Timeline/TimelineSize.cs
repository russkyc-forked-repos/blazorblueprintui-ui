namespace BlazorBlueprint.Components.Timeline;

/// <summary>
/// Defines the size variant for Timeline components.
/// </summary>
/// <remarks>
/// Controls the gap spacing between timeline items and the size of icons.
/// </remarks>
public enum TimelineSize
{
    /// <summary>
    /// Small size with compact spacing (gap-4).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size with moderate spacing (gap-6). This is the default.
    /// </summary>
    Medium,

    /// <summary>
    /// Large size with generous spacing (gap-8).
    /// </summary>
    Large
}
