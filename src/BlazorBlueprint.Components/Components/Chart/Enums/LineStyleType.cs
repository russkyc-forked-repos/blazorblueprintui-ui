namespace BlazorBlueprint.Components;

/// <summary>
/// Specifies the visual style of a line (axis lines, grid lines, borders).
/// </summary>
public enum LineStyleType
{
    /// <summary>
    /// A continuous solid line.
    /// </summary>
    Solid,

    /// <summary>
    /// A dashed line (default for grid lines).
    /// </summary>
    Dashed,

    /// <summary>
    /// A dotted line.
    /// </summary>
    Dotted
}
