namespace BlazorBlueprint.Components;

/// <summary>
/// Defines the size variant for a Toast component.
/// </summary>
public enum ToastSize
{
    /// <summary>
    /// Default toast size with standard padding and font sizes.
    /// </summary>
    Default,

    /// <summary>
    /// Compact toast size with reduced padding and smaller text.
    /// Suitable for dialog-embedded toasts or dense layouts.
    /// </summary>
    Compact
}
