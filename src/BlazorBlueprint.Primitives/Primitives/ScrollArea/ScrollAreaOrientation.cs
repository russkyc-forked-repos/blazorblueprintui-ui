namespace BlazorBlueprint.Primitives.ScrollArea;

/// <summary>
/// Defines the scrollable orientation of a scroll area.
/// </summary>
public enum ScrollAreaOrientation
{
    /// <summary>
    /// Vertical scrolling only.
    /// </summary>
    Vertical,

    /// <summary>
    /// Horizontal scrolling only.
    /// </summary>
    Horizontal,

    /// <summary>
    /// Both vertical and horizontal scrolling.
    /// </summary>
    Both
}
